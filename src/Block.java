import java.util.Date;

public class Block {
    // this is a block
    public static void main(String[] args) {

        // this is expressions
        int age = 22;

        // this is statements
        // assign statement
        double aValue = 933.2;
        // increment statement
        aValue++;
        // method invocation statement
        System.out.println(aValue);
        //object creation statement
        Date date = new Date();
    }
}
